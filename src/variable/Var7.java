package variable;

public class Var7 {

    public static void main(String[] args) {
        // 변수의 타입(형, 형식 이라고도 한다.)
        int     a   = 100;          // int      : 정수 타입
        double  b   = 12.26;        // double   : 실수 타입
        boolean c   = true;         // boolean  : 불리언(boolean)타입 true/false 입력 가능
        char    d   = 'A';          // char     : 문자 타입 (하나) 입력 가능
        String  e   = "Hello Java"; // String   : 문자열 타입 (문자 여러개) 입력 가능

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        /*
        * 리터럴 (literal)
        *   - 개발자가 직접 입력한 고정된 값.
        *   - 변수의 값은 변할 수 있지만 리터럴 자체는 변하지 않는다.
        * */
        // 100          : 정수 리터럴
        // 12.26        : 실수 리터럴
        // true         : 불리언 리터럴
        // A            : 문자 (하나) 리터럴
        // Hello Java   : 문자열 리터럴
    }


}
