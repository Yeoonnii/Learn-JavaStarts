package variable;

public class Var5 {
    public static void main(String[] args) {
        // 1. 하나의 변수 선언, 변수 초기화를 따로
        int a;
        a = 5;
        System.out.println(a);

        // 2. 하나의 변수 선언과 변수 초기화를 동시에
        int b = 10;
        System.out.println(b);

        // 3. 여러개의 변수 선언, 변수 초기화를 동시에
        int c = 12, d = 17;
        System.out.println(c);
        System.out.println(d);

    }
}
